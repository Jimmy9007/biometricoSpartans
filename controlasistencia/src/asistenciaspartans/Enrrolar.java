    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        String documento = JOptionPane.showInputDialog("Digite el documento de identidad:");
        try {
            Biometrico biometricoOBJ = new Biometrico();
            RespuestaServidor respuestaOBJ = biometricoOBJ.buscarUsuario(documento);
            if (respuestaOBJ.getResponseCode() == 200) {
                if (respuestaOBJ.getUsuarioExiste() == 0) {
                    JOptionPane.showMessageDialog(this, "Usuario no existe", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    String cadenaNombre = Normalizer.normalize(respuestaOBJ.getNombreUsuario(), Normalizer.Form.NFD);
                    String nombreSinAcentos = cadenaNombre.replaceAll("[^\\p{ASCII}]", "");
                    jTDocumento.setText(respuestaOBJ.getDocumentoUsuario());
                    jTNombre.setText(nombreSinAcentos);
                    jFTFechaNacimiento.setText(respuestaOBJ.getFechaNacimiento());
                    jTIdUsuario.setText(respuestaOBJ.getIdClienteEmpleado());
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(Enrrolar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBEnrrolarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEnrrolarActionPerformed
        String documento = jTDocumento.getText();
        String nombre = jTNombre.getText();
        String fechaNac = jFTFechaNacimiento.getText();

        if (documento.length() == 0 || nombre.length() == 0 || fechaNac.length() == 0) {
            JOptionPane.showMessageDialog(this, "Los campos no deben estar vacios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            principal.onEnroll();
        }
    }//GEN-LAST:event_jBEnrrolarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        String documento = jTDocumento.getText();
        String idClienteEmpleado = jTIdUsuario.getText();
        String nombre = jTNombre.getText();
        String fechaNacimiento = jFTFechaNacimiento.getText();

        if (documento.length() == 0 || nombre.length() == 0 || fechaNacimiento.length() == 0) {
            JOptionPane.showMessageDialog(this, "Los campos no deben estar vacios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (principal != null) {
            if (principal.template == null) {
                JOptionPane.showMessageDialog(this, "Debes enrrolar una huella", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                try {
                    int pregunta = JOptionPane.showConfirmDialog(this, "Â¿Desea guardar la informacion?", "Pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    if (pregunta == 0) {
                        Biometrico biometricoOBJ = new Biometrico();
                        RespuestaServidor respuestaOBJ = biometricoOBJ.onSave(documento, idClienteEmpleado, nombre, fechaNacimiento, principal.getTemplate(), DPFPGlobal.getSampleConversionFactory().createImage(principal.getSampleAux()));
                        if (respuestaOBJ.getUsuarioExiste() == 1) {
                            JOptionPane.showMessageDialog(this, "Error, el usuario " + nombre + "\nya se encuentra enrrolado anteriormente.", "Error", JOptionPane.ERROR_MESSAGE);
                            DesktopNotify.showDesktopMessage("Mensaje de Error", "El usuario " + nombre + " ya se encuentra enrrolado anteriormente.", DesktopNotify.ERROR);
                            principal.template = null;
                            jTDocumento.setText("");
                            jTIdUsuario.setText("");
                            jTNombre.setText("");
                            jFTFechaNacimiento.setText("");
                            jTDocumento.requestFocus();
                        } else {
                            JOptionPane.showMessageDialog(this, "Correcto, el usuario " + nombre + "\nya se encuentra enrrolado.", "Enrrolamiento con exito", JOptionPane.INFORMATION_MESSAGE);
                            DesktopNotify.showDesktopMessage("Enrrolamiento con exito", "Usuario " + nombre + " ya se encuentra enrrolado.", DesktopNotify.SUCCESS);
                            principal.template = null;
                            jTDocumento.setText("");
                            jTIdUsuario.setText("");
                            jTNombre.setText("");
                            jFTFechaNacimiento.setText("");
                            jTDocumento.requestFocus();
                        }
                    }
                } catch (IOException ex) {
                    Logger.getLogger(Enrrolar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debes enrrolar una huella", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

